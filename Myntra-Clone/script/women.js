function handleSort() {
  var selected = document.querySelector("#type").value;

  if (selected == "1") {
    womensData.sort(function (a, b) {
      return b.price - a.price;
    });
    displayData(womensData);
  }

  if (selected == "2") {
    womensData.sort(function (a, b) {
      return a.price - b.price;
    });
    displayData(womensData);
  }

}



  function displayData(womensData) {
    document.querySelector(".products").innerHTML = "";
   womensData.forEach(function (elem){
       var card = document.createElement("div");
       card.setAttribute("class","product-card")


       var img = document.createElement("img");
       img.src = elem.img;
       img.style.cursor = "pointer";
       // img.onclick = "window.location.href='../html/details.html'";

   
       var name = document.createElement("p");
       name.textContent = elem.name;
       name.setAttribute("class", "itemname");
       name.style.cursor = "pointer";
       name.style.color = "black";
       name.style.fontSize = "13px"
       name.style.marginTop = "5px";
       // name.onclick = "redirect()";
   
       var price = document.createElement("p");
       price.innerText = "Rs." + elem.price;

       var strike = document.createElement("p");
       strike.innerText = "Rs." + elem.strikedoffprice;


       var priceflex = document.createElement("div");
       priceflex.setAttribute("class", "priceflex");

       var btn = document.createElement("button");
       btn.innerText = "Add to Bag";
       btn.setAttribute("class", "btn");
       btn.addEventListener("click", function(){
        addToCart(elem)
      });
       
       priceflex.append(price, strike);
       card.append(img, name, priceflex, btn);

       document.querySelector(".products").append(card);


   });

  }


   function addToCart(elem){
    cartData.push(elem);
    alert("Added to Bag");
    localStorage.setItem("cart", JSON.stringify(cartData))
  }

  